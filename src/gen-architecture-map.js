#!/usr/bin/env node
// ARCHITECTURE.md Generator — reads plugin structure and generates architecture overview.
// CLI: (no flag)→stdout | --write→file | --check→diff check
// Zero npm dependencies. Node.js built-in only.
const fs = require('fs');
const path = require('path');

const ROOT = path.join(__dirname, '..');
const AGENTS_DIR = path.join(ROOT, 'agents');
const SKILLS_DIR = path.join(ROOT, 'skills');
const COMMANDS_DIR = path.join(ROOT, 'commands');
const HOOKS_DIR = path.join(ROOT, 'hooks');
const LAYER_MAP = path.join(AGENTS_DIR, '_shared', 'layer-map.json');
const PLUGIN_JSON = path.join(ROOT, '.claude-plugin', 'plugin.json');
const OUTPUT = path.join(ROOT, 'ARCHITECTURE.md');

// ─── Helpers ────────────────────────────────────────────────────────

function parseFrontmatter(content) {
  const lines = content.split('\n');
  if (lines[0].trim() !== '---') return {};
  const meta = {};
  for (let i = 1; i < lines.length; i++) {
    if (lines[i].trim() === '---') break;
    if (lines[i].startsWith('<') || lines[i].startsWith(' ')) continue;
    const m = lines[i].match(/^(\w+):\s*(.+)$/);
    if (m) {
      let val = m[2].trim();
      if (val.startsWith('[')) { try { val = JSON.parse(val); } catch (_) {} }
      meta[m[1]] = val;
    }
  }
  return meta;
}

function readAgents() {
  try {
    return fs.readdirSync(AGENTS_DIR)
      .filter(f => f.endsWith('.md') && !f.startsWith('_'))
      .map(f => {
        const content = fs.readFileSync(path.join(AGENTS_DIR, f), 'utf-8');
        const meta = parseFrontmatter(content);
        return { ...meta, _file: f.replace('.md', '') };
      })
      .filter(a => a.name);
  } catch { return []; }
}

function loadLayers() {
  try {
    const data = JSON.parse(fs.readFileSync(LAYER_MAP, 'utf-8'));
    const map = {};
    const layerList = [];
    for (const [name, agents] of Object.entries(data.layers)) {
      for (const a of agents) map[a] = name;
      layerList.push({ name, agents });
    }
    return { layers: layerList, agentLayer: map };
  } catch { return { layers: [], agentLayer: {} }; }
}

function listSkills() {
  try {
    return fs.readdirSync(SKILLS_DIR)
      .filter(f => !f.endsWith('.md'))
      .sort();
  } catch { return []; }
}

function listCommands() {
  try {
    return fs.readdirSync(COMMANDS_DIR)
      .filter(f => f.endsWith('.md'))
      .map(f => f.replace('.md', ''))
      .sort();
  } catch { return []; }
}

function listHooks() {
  try {
    const hooksJson = path.join(HOOKS_DIR, 'hooks.json');
    const data = JSON.parse(fs.readFileSync(hooksJson, 'utf-8'));
    return Object.keys(data.hooks || {});
  } catch { return []; }
}

function getVersion() {
  try { return JSON.parse(fs.readFileSync(PLUGIN_JSON, 'utf-8')).version || '?'; } catch { return '?'; }
}

function shortDesc(d) {
  if (!d) return '';
  const s = d.split(/[.!]/)[0];
  return s.length > 60 ? s.slice(0, 57) + '...' : s;
}

// ─── Generator ──────────────────────────────────────────────────────

function generate() {
  const agents = readAgents();
  const { layers, agentLayer } = loadLayers();
  const skills = listSkills();
  const commands = listCommands();
  const hookEvents = listHooks();
  const version = getVersion();
  const now = new Date().toISOString().split('T')[0];

  const L = [];

  // Header
  L.push(
    '# ARCHITECTURE.md — Team-Shinchan Plugin Architecture',
    '',
    `> Auto-generated by \`src/gen-architecture-map.js\`. Do not edit manually.`,
    `> Plugin version: ${version} | Generated: ${now}`,
    ''
  );

  // ── 1. System Overview ──────────────────────────────────────────
  L.push(
    '## 1. System Overview',
    '',
    'Team-Shinchan is a multi-agent orchestration plugin for Claude Code.',
    'It enforces a 4-stage workflow (Requirements → Planning → Execution → Completion)',
    'through specialized agents, hooks, and harness-lint checks.',
    '',
    '### Agent Layer Hierarchy',
    '',
    '```'
  );
  for (const ly of layers) {
    L.push(`  [${ly.name.toUpperCase()}]`);
    for (const a of ly.agents) {
      const agent = agents.find(ag => ag.name === a);
      const desc = agent ? shortDesc(agent.description) : '';
      L.push(`    ├─ ${a}${desc ? '  — ' + desc : ''}`);
    }
  }
  L.push('```', '');

  // Agent count summary
  L.push(`Total agents: ${agents.length} | Layers: ${layers.length}`, '');

  // ── 2. Workflow 4-Stage Flow ────────────────────────────────────
  L.push(
    '## 2. Workflow: 4-Stage Flow',
    '',
    '```',
    '  ┌─────────────────────────────────────────────────────┐',
    '  │  Stage 1: Requirements                              │',
    '  │  Owner: misae  →  Output: REQUESTS.md              │',
    '  │  Gate: Problem Statement + AC + user approval       │',
    '  └──────────────────────┬──────────────────────────────┘',
    '                         ↓',
    '  ┌─────────────────────────────────────────────────────┐',
    '  │  Stage 2: Planning                                  │',
    '  │  Owner: nene   →  Output: PROGRESS.md              │',
    '  │  Gate: Phased plan with testable AC                 │',
    '  └──────────────────────┬──────────────────────────────┘',
    '                         ↓',
    '  ┌─────────────────────────────────────────────────────┐',
    '  │  Stage 3: Execution                                 │',
    '  │  Owner: bo/aichan/bunta/masao/kazama                │',
    '  │  Gate: Action Kamen review per phase                │',
    '  └──────────────────────┬──────────────────────────────┘',
    '                         ↓',
    '  ┌─────────────────────────────────────────────────────┐',
    '  │  Stage 4: Completion                                │',
    '  │  Owner: masumi →  Output: RETROSPECTIVE.md,        │',
    '  │                            IMPLEMENTATION.md        │',
    '  │  Gate: Action Kamen final pass + all AC checked     │',
    '  └─────────────────────────────────────────────────────┘',
    '```',
    ''
  );

  // ── 3. Immutable Rules ─────────────────────────────────────────
  L.push(
    '## 3. Immutable Rules (Harness Constraints)',
    '',
    '> These rules are enforced by hooks and harness-lint. Violations block execution.',
    '',
    '### Delegation Rules',
    '- Shinnosuke (orchestrator) NEVER implements directly — always delegates to specialists',
    '- Stage 1 (requirements) → Misae only',
    '- Stage 2 (planning) → Nene only',
    '- Stage 3 (execution) → Bo, Aichan, Bunta, Masao, Kazama',
    '- Every phase completion requires Action Kamen review (no exceptions)',
    '',
    '### Stage-Tool Restrictions',
    '| Stage | Allowed | Blocked |',
    '|-------|---------|---------|',
    '| requirements | Read, Glob, Grep, Task | Edit, Write, Bash |',
    '| planning | Read, Glob, Grep, Write | Edit, Bash |',
    '| execution | All tools | — |',
    '| completion | Read, Write, Task | Bash (destructive) |',
    '',
    '### Agent Layer Restrictions',
    '- Execution agents (bo, kazama) cannot call Planning/Orchestration agents directly',
    '- Utility agents (shiro, masumi, ume) cannot call any other agents',
    '- All agents can call actionkamen (exception by design)',
    '',
    '### Design Decision Trigger',
    '- 2+ approaches, architecture change, pattern break, or tech stack → Midori debate required',
    '- Debate result must be presented to user before proceeding',
    ''
  );

  // ── 4. Boundary Setting ────────────────────────────────────────
  L.push(
    '## 4. Boundaries',
    '',
    '### Plugin Root (Read-only structure)',
    '```',
    '  team-shinchan/',
    '  ├── agents/          ← 15 agent definition files',
    '  ├── skills/          ← ' + skills.length + ' skill implementations',
    '  ├── commands/        ← ' + commands.length + ' slash commands',
    '  ├── hooks/           ← Event-driven automation (' + hookEvents.length + ' events)',
    '  ├── src/             ← Node.js scripts (zero npm deps)',
    '  ├── tests/           ← Validators and CI checks',
    '  ├── docs/            ← Workflow guides',
    '  └── .claude-plugin/  ← Plugin manifest',
    '```',
    '',
    '### Project-Level Isolation (.gitignored)',
    '```',
    '  .shinchan-docs/      ← Workflow state (WORKFLOW_STATE.yaml, REQUESTS.md, PROGRESS.md)',
    '  .shinchan-docs/ontology/   ← Project ontology (ontology.json)',
    '  .shinchan-docs/learnings.md',
    '  .shinchan-docs/kb-summary.md',
    '  .shinchan-docs/agent-context-cache.json',
    '```',
    ''
  );

  // ── 5. Key Entry Points ────────────────────────────────────────
  L.push(
    '## 5. Key Entry Points',
    '',
    '### Skills → Commands (/' + skills.length + ' total)',
    '> Each skill in `skills/` has a paired command in `commands/`.',
    ''
  );

  // Show top 15 skills as representative sample (keep under 200 lines)
  const sampleSkills = skills.slice(0, 15);
  L.push('| Skill / Command | Purpose |');
  L.push('|-----------------|---------|');
  for (const s of sampleSkills) {
    const cmdPath = path.join(COMMANDS_DIR, s + '.md');
    let desc = '';
    try {
      const cmdContent = fs.readFileSync(cmdPath, 'utf-8');
      const m = cmdContent.match(/description:\s*(.+)/);
      if (m) desc = shortDesc(m[1].replace(/['"]/g, ''));
    } catch {}
    L.push(`| \`/team-shinchan:${s}\` | ${desc || '—'} |`);
  }
  if (skills.length > 15) L.push(`| *(${skills.length - 15} more — see \`commands/\`)* | |`);
  L.push('');

  // Hook events
  L.push(
    '### Hooks (Event-Driven Automation)',
    '',
    '| Event | Purpose |',
    '|-------|---------|',
    '| SessionStart | session-init.sh — KB load, learnings, interrupted workflow detection |',
    '| PreToolUse | security-check, deny-check, agent-tool-guard, workflow-guard, budget-guard |',
    '| PostToolUse | write-tracker, layer-guard, scope-guard |',
    '| SubagentStart/Stop | write-tracker (timing), trace-init |',
    '| Stop | session-wrap (budget persist), auto-retrospective |',
    ''
  );

  // ── 6. Quick Reference ─────────────────────────────────────────
  L.push(
    '## 6. Quick Reference',
    '',
    '| Need | Use |',
    '|------|-----|',
    '| Start new feature | `/team-shinchan:start` |',
    '| Check workflow status | `/team-shinchan:status` |',
    '| Resume interrupted work | `/team-shinchan:resume` |',
    '| Run all validators | `/team-shinchan:verify-implementation` |',
    '| Debate design decision | Trigger via Shinnosuke → Midori |',
    '| Analyze codebase impact | `/team-shinchan:impact-analysis` |',
    '| Run harness lint | `node src/harness-lint.js --format table` |',
    '| Rebuild architecture map | `node src/gen-architecture-map.js --write` |',
    ''
  );

  const result = L.join('\n') + '\n';

  // Enforce 200-line limit (warn only, don't truncate — content is auto-generated)
  const lineCount = result.split('\n').length;
  if (lineCount > 200) {
    process.stderr.write(`Warning: ARCHITECTURE.md is ${lineCount} lines (limit: 200). Consider trimming.\n`);
  }

  return result;
}

// ── Exports ─────────────────────────────────────────────────────────
module.exports = { generate };

// ─── CLI ────────────────────────────────────────────────────────────

if (require.main === module) {
  const flag = process.argv[2];
  const content = generate();

  if (flag === '--write') {
    fs.writeFileSync(OUTPUT, content, 'utf-8');
    const lines = content.split('\n').length;
    console.log(`Wrote ARCHITECTURE.md (${lines} lines)`);
  } else if (flag === '--check') {
    if (!fs.existsSync(OUTPUT)) {
      console.error('ARCHITECTURE.md not found. Run: node src/gen-architecture-map.js --write');
      process.exit(1);
    }
    if (fs.readFileSync(OUTPUT, 'utf-8') !== content) {
      console.error('ARCHITECTURE.md is out of date. Run: node src/gen-architecture-map.js --write');
      process.exit(1);
    }
    console.log('ARCHITECTURE.md is up to date.');
  } else {
    process.stdout.write(content);
  }
}
